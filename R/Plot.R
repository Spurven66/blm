#' Plot
#'
#' This function plots blm models
#'
#' @param model   A formula describing the model of class blm
#' @param ...     Additifonal data, for example a data frame. Feel free to add other options.
#'
#' @return x-y plot with a line going through it for the fit
#' @export


This function plots your model. You are pretty free to decide how you want to plot it,
but I could imagine that it would be useful to see an x-y plot with a line going through it for the fit.
If there are more than one predictor variable, though,
I am not sure what would be a good way to visualise the fitted model.
There are no explicit rules for what the `plot` function should do,
except for plotting something so you can use your imagination.